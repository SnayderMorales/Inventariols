    private void BtnRegresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegresarActionPerformed

        if (bandera != 0) {
            nuevo = JOptionPane.showConfirmDialog(null, "Recuerde Que Al Momento De Salir Se Perdera El Progreso.\nSeguro Desea Salir?", "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (nuevo == 0) {
                try {
                    ingreso.borrar(ingreso.getCodigo());
                } catch (SQLException ex) {
                    Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
                }

                Index index = new Index();
                index.setVisible(true);
                this.setVisible(false);

            }

        } else {
            Index index = new Index();
            index.setVisible(true);
            this.setVisible(false);

        }


    }//GEN-LAST:event_BtnRegresarActionPerformed

    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed

        if (bandera == 0) {
            CrearIngreso();
            vectores();
            ingreso = ingre.get(ingre.size() - 1);
            bandera = 1;

        }
        try {
            pro.add(new Producto(0, TxtDesc.getText(), Integer.parseInt(TxtCant.getText()), null));
            TablaIn.setModel(new ModeloTablasInOut(pro));
            TxtDesc.setText("");
            TxtCant.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Dato Ingresado No Es Correcto!!");
        }


    }//GEN-LAST:event_BtnAddActionPerformed

    private void TablaInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaInMouseClicked

        if (TablaIn.getSelectedColumn() == 1) {
            TxtDesc.setText(TablaIn.getValueAt(TablaIn.getSelectedRow(), TablaIn.getSelectedColumn() - 1).toString());
            TxtCant.setText(TablaIn.getValueAt(TablaIn.getSelectedRow(), TablaIn.getSelectedColumn()).toString());

        } else {
            TxtDesc.setText(TablaIn.getValueAt(TablaIn.getSelectedRow(), TablaIn.getSelectedColumn()).toString());
            TxtCant.setText(TablaIn.getValueAt(TablaIn.getSelectedRow(), TablaIn.getSelectedColumn() + 1).toString());
        }


    }//GEN-LAST:event_TablaInMouseClicked

