    private void BtnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEnviarActionPerformed

        int cant = 0;
        for (Producto pr : pro) {
            for (Producto p : lista) {
                if (pr.getDescripcion().equals(p.getDescripcion())) {
                    ing_pro.add(new Ingreso_Productos(ingreso, p, (double) pr.getTotal()));
                }
            }
        }

        
        for (Ingreso_Productos in : ing_pro) {
            try {
                i.crear(in);
            } catch (SQLException ex) {
                Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(null, "Sus Datos Han Sido Correctamente Ingresados!");

        pro.clear();
        TablaIn.setModel(new ModeloTablasInOut(pro));
        MainTable.setModel(new Modelo());
    }//GEN-LAST:event_BtnEnviarActionPerformed

    public void BorrarFact() {
        try {
            for (Producto p : pro) {
                if (p.getDescripcion().equals(TxtDesc.getText())) {
                    pro.remove(p);
                }
            }
        } catch (ConcurrentModificationException e) {

        }
    }

