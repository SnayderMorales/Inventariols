    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        boolean esta = false;
        int cantidad =0;
        try {

            Producto nuevo = new Producto();
            nuevo.setDescripcion(TxtDesc.getText());// = new Producto(0, , Integer.parseInt(TxtCant.getText()), null);
            nuevo.setTotal(Integer.parseInt(TxtCant.getText()));
            for (Producto pr : lista) {
            if (pr.getDescripcion().equals(nuevo.getDescripcion()) && nuevo.getTotal() <= pr.getTotal()) {
                esta = true;
                break;
            }else if(pr.getDescripcion().equals(nuevo.getDescripcion()) && nuevo.getTotal() > pr.getTotal()){
                esta = false;
                cantidad = pr.getTotal();
                break;
            }
        }
        if (esta == true) {
            pro.add(nuevo);
            TablaOut.setModel(new ModeloTablasInOut(pro));
            TxtDesc.setText("");
            TxtCant.setText("");
            
        } else {
            JOptionPane.showMessageDialog(null, "CANTIDAD NO DISPOIBLE EN BODEGA.\n SOLO EXISTEN ESTAS UNIDADES: " + cantidad);
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Dato Ingresado No Es Correcto!!");
        }
        


    }//GEN-LAST:event_jButton1ActionPerformed

