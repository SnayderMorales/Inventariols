    public void BorrarFact() {
        try {
            for (Producto p : pro) {
                if (p.getDescripcion().equals(TxtDesc.getText())) {

                    pro.remove(p);

                }
            }
        } catch (ConcurrentModificationException e) {

        }
    }

    public void Salidas() {
        for (Producto p : pro) {
            for (Producto pr : lista) {
                if (p.getDescripcion().equals(pr.getDescripcion())) {
                    ListaSp.add(new Salida_Productos(salida, pr, p.getTotal()));
                }
            }
        }
    }

    public void SalidasProductos() {
        for (Salida_Productos sp : ListaSp) {
            try {
                SP.crear(sp);
            } catch (SQLException ex) {
                Logger.getLogger(Salidas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Salidas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }

