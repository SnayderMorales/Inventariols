    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        int locker = 0;
        int nuevo = 0;
        boolean esta = false;
        try {
            int locke = Integer.parseInt(txtlocker.getText());

            for (Locker l : loc) {
                if (l.getNumero() == locke) {
                    esta = true;
                    locker = l.getId();
                }
            }
            if (esta == true) {
                loc_ocu(locker);
                txtdescripcion.setText("");
                txtlocker.setText("");
            } else {
                nuevo = JOptionPane.showConfirmDialog(null, "USTED ACABA DE INGRESAR UN NUMERO DE LOCKER \nQUE NO SE ENCUENTRA EN LA BASE DE DATOS\nDESEA USTED QUE ESTE LO QUE SEA INGRESADO?", "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (nuevo == 0) {
                    try {
                        pr.crearLocker(new Locker(0, Integer.parseInt(txtlocker.getText())));
                    } catch (SQLException ex) {
                        Logger.getLogger(NuevoProducto.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(NuevoProducto.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    datos();
                } else {

                    JOptionPane.showMessageDialog(null, "CORRIJA EL DATO POR FAVOR!!");
                }

            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Dato Ingresado No Es Correcto!!");
        }


    }//GEN-LAST:event_btnguardarActionPerformed

    public void loc_ocu(int locker) {
        try {
            String descripcion = txtdescripcion.getText();

            Producto p = new Producto();
            p.setDescripcion(descripcion);

            p.setLocker(new Locker(locker, 0));
            p.crear(p);
        } catch (SQLException ex) {
            Logger.getLogger(NuevoProducto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NuevoProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

